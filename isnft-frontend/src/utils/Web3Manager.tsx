export const contractAddress: string =
  '0x34494838740f7Fd02ADA7045788dF7898A7f7112';

export const connectMetamask = async () => {
  if (!window.ethereum) {
    return;
  }
  window.web3 = window.web3 || {};
  window.web3 = new Web3(window.web3.currentProvider);

  try {
    await window.ethereum.enable();
    return 'success';
  } catch {
    return 'failed';
  }
};

export const mintNFT = async (tokenURI: string) => {
  if (!window.ethereum) {
    return;
  }
  const connectedStatus = await connectMetamask();
  if (connectedStatus === 'success') {
    const myContractInstance = new window.web3.eth.Contract(
      abi,
      contractAddress
    );
    const transactionParameters = {
      to: contractAddress,
      from: window.ethereum.selectedAddress,
      data: myContractInstance.methods
        .mintNFT(window.ethereum.selectedAddress, tokenURI)
        .encodeABI(), //make call to NFT smart contract
    };
    const txHash = await window.ethereum.request({
      method: 'eth_sendTransaction',
      params: [transactionParameters],
    });
    return txHash;
  } else {
    console.log('metamask connection failed');
  }
};
